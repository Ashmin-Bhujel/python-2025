# Add function
def add(x, y):
    print(f"Result: {x + y}")


# Subtract function
def subtract(x, y):
    print(f"Result: {x - y}")


# Multipy function
def multipy(x, y):
    print(f"Result: {x * y}")


# Division function
def divide(x, y):
    print(f"Result: {x / y}")


# Power function
def pow(x, y):
    print(f"Result: {x ** y}")
